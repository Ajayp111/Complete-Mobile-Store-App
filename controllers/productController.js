// filters
export const productFiltersController = async (req, res) => {
  try {
    const { checked, radio } = req.body; // Destructuring 'checked' and 'radio' values from the request body .
    let args = {}; // Initializing an empty object to build filtering criteria.

    if (checked.length > 0) args.category = checked; // Adding 'category' filter if categories are selected
    if (radio.length) args.price = { $gte: radio[0], $lte: radio[1] }; // Checking if there is a selected price range for filtering
    const products = await productModel.find(args); // Finding products based on the constructed filter criteria
    res.status(200).send({
      success: true,
      products, // Sending the filtered products in the response
    });
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "Error WHile Filtering Products",
      error,
    });
  }
};

